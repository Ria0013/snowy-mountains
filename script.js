let currentPage = 
Number(localStorage.getItem("snowPage")) || 0;


let fontSize = 17;

let typingTimer;

let typingID = 0;





/* =========================
   페이지 출력
========================= */


function loadPage(){

    typingID++;

    clearTimeout(typingTimer);


    const content = document.getElementById("content");


    content.innerHTML = "";


    typeWriter(
        pages[currentPage],
        content
    );


    document.getElementById("pageNumber").innerText =
    String(currentPage + 1).padStart(2,"0");


    document.getElementById("totalPage").innerText =
    pages.length;



    document.getElementById("progressFill").style.width =
    ((currentPage + 1) / pages.length * 100)
    + "%";



    localStorage.setItem(
        "snowPage",
        currentPage
    );



    document.getElementById("prev").disabled =
    currentPage === 0;



    document.getElementById("next").disabled =
    currentPage === pages.length - 1;


}







/* =========================
   타자 효과
========================= */


function typeWriter(text, element){

    clearTimeout(typingTimer);

    let i = 0;

    const speed = 25;

    const thisID = typingID;


    function typing(){


        if(thisID !== typingID){

            return;

        }


        if(i < text.length){


            element.innerHTML += text.charAt(i);


            i++;


            typingTimer = setTimeout(
                typing,
                speed
            );


        }


    }


    typing();

}






/* =========================
   페이지 이동
========================= */


document.getElementById("next")
.onclick = function(){


    if(currentPage < pages.length - 1){


        currentPage++;


        loadPage();


    }


};





document.getElementById("prev")
.onclick = function(){


    if(currentPage > 0){


        currentPage--;


        loadPage();


    }


};







/* =========================
   키보드 이동
========================= */


document.addEventListener(
"keydown",
function(e){


    if(e.key === "ArrowRight"){

        document.getElementById("next").click();

    }



    if(e.key === "ArrowLeft"){

        document.getElementById("prev").click();

    }


});








/* =========================
   글자 크기
========================= */


document.getElementById("bigFont")
.onclick=function(){


    fontSize += 2;


    document.getElementById("content")
    .style.fontSize = fontSize+"px";


};





document.getElementById("smallFont")
.onclick=function(){


    if(fontSize > 10){

        fontSize -= 2;

    }


    document.getElementById("content")
    .style.fontSize = fontSize+"px";


};








/* =========================
   눈 생성
========================= */


function createSnow(){


    const snow =
    document.getElementById("snow");



    for(let i=0;i<80;i++){


        let flake =
        document.createElement("div");



        flake.className =
        "snowflake";



        let size =
        Math.random()*5+2;



        flake.style.width =
        size+"px";



        flake.style.height =
        size+"px";



        flake.style.left =
        Math.random()*100+"%";



        flake.style.animationDuration =
        Math.random()*8+5+"s";



        flake.style.animationDelay =
        Math.random()*5+"s";



        snow.appendChild(flake);


    }


}







/* =========================
   로딩 종료
========================= */


window.onload=function(){


    createSnow();


    setTimeout(()=>{


        document.getElementById("loading")
        .style.display="none";


        loadPage();


    },2200);


};
